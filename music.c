#include "music.h"


void tone(uint pin, uint frequency, uint duration_ms) {
    uint slice_num = pwm_gpio_to_slice_num(pin);
    uint wrap = 12500;
    float divider = (float)clock_get_hz(clk_sys) / (wrap * frequency);

    pwm_set_wrap(slice_num, wrap);
    pwm_set_clkdiv(slice_num, divider);
    pwm_set_chan_level(slice_num, pwm_gpio_to_channel(pin), wrap / 2);

    sleep_ms(duration_ms);
    pwm_set_chan_level(slice_num, pwm_gpio_to_channel(pin), 0);
    sleep_ms(50);
}

